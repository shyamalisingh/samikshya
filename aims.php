    <h4>Our Aims and objectives:</h4>
    <hr/>
    <ul>
        <li>To promote the cause of national integration and unity and to fight against the forces of separation in India.</li>
        <li>
            To render services to the women, children & youths of the country for their mental and social health and rights.

        </li>
        <li>
           To promote literacy, cultural and other social activities by awareness programme, Adult education, classes,
            lectures, essay competitions, exhibitions, symposiums, cultural programs, press conferences and seminars.

        </li>
        <li>
            To make adequate arrangements and start a rehabilitation program for destitute and underprivileged.
        </li>
        <li>
           To open the institutes & hostels for the handicapped, deaf, dumb and blind.
        </li>
        <li>
        To start various programmes against child labour, human trafficking and saving of the girl child.
        </li>
        <li>
       To provide free legal, medical and spiritual help to the needy and poor.
        </li>
         <li>
       To provide information and education for the patients suffering from deadly diseases and to help them to recover soon
             after societal stigma.
        </li>
        <li>
      To open centre for research and diagnosis, Especially for the patient who are unaffordable and needy.
        </li>
        <li>
        To organise puppet shows, <i>nukkad, nataks</i>  and short plays just for the sake of providing education and awareness amongst the deprived and underprivileged class of our society.
        </li>
        <li>
        To provide medical emergency vehicle and  tools for the needy and deprived ones viz pregnant ladies, cardiac arrest patients and miscellaneous cases which require acute medical interventions.
        </li>

    </ul>

   
